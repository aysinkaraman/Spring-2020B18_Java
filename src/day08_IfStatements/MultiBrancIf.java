package day08_IfStatements;

public class MultiBrancIf {
    public static void main(String[] args) {
        int a = 100;
         if (a == 0) {
             System.out.println("zero");
         } else if (a>0) {
             System.out.println("Positive");
         } else {
             System.out.println("Negative");
         }


    }
}
